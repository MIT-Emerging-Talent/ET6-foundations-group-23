def add_numbers(a, b):
    """
    Computes the sum of two numbers.

    Parameters:
        a (int or float): The first number.
        b (int or float): The second number.

    Returns:
        int or float: The sum of the two numbers.

    Examples:
        >>> add_numbers(3, 5)
        8
        >>> add_numbers(-2, 4)
        2
        >>> add_numbers(3.5, 2.5)
        6.0
    """
    return a + b
